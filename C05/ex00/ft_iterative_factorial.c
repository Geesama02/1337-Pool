int	ft_iterative_factorial(int nb)
{
	int	l;

	l = nb;
	if (nb < 0)
		return (0);
	if (nb == 0)
		return (1);
	while (l-- > 1)
	{
		nb = nb * l;
	}
	return (nb);
}
