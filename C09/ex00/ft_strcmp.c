int	ft_strcmp(char *s1, char *s2)
{
	int	r;
	int	i;

	i = 0;
	r = 0;
	if (s1[i] == '\0')
	{
		r = s1[i] - s2[i];
		return (r);
	}
	while (s1[i] != '\0' || s2[i] != '\0')
	{
		if (s1[i] != s2[i])
		{
			r = s1[i] - s2[i];
			return (r);
		}
		i++;
	}
	return (r);
}
